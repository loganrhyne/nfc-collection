// Stagger animation helper
export const staggerChildren = (delay = 50) => `
  > * {
    opacity: 0;
    animation: ${fadeIn} 0.5s ease-out forwards;
    
    ${Array.from({ length: 20 }, (_, i) => `
      &:nth-child(${i + 1}) {
        animation-delay: ${i * delay}ms;
      }
    `).join('')}
  }
`;

